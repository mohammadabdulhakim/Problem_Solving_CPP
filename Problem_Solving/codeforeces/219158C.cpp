#include <iostream>
using namespace std;
 
int main(){
    long long fNum, sNum; cin >> fNum >> sNum;
    
    cout << fNum << " + " << sNum << " = " << fNum + sNum  << '\n';
    cout << fNum << " * " << sNum << " = " << fNum * sNum  << '\n';
    cout << fNum << " - " << sNum << " = " << fNum - sNum  << '\n';
    return 0;
}